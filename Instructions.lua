		Command Options (required):
		User               The Username of a victim.
		Integer            Integer from 0.0 to 10000.0
		Number			   Integer, but rounded.
		Vector3            Location in a third-dimensional area.
		Vector2            Location in a second-dimensional area.
		String             A line of text.
		ID				   Roblox Asset Identification numbers. must be compatible with rbxassetid://
		
		
		
